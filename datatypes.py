#data types

#integers
a=123
print(type(a))

#float
b=12.3
print(type(b))

#boolean
c=True
print(type(c))

#string
d='hello'
print(type(d))

#complex
e=2+3j
print(type(e))

#list
f=[1,23.3,'hii',True]
print(type(f))

#tuple
g=(1,3,5,47)
print(type(g))

#set
h={23,55.3,'hello',10.005}
print(type(h))

#dictonary
i={1:"hii",2:23.005,3:False}
print(type(i))

#frozenset
j=[{1,23,222,667,7}]
print(type(j))
